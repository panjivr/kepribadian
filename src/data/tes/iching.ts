export interface Garis {
  yang: boolean;
  berubah: boolean;
}

/** Lempar 3 koin: kepala=3, ekor=2. Jumlah 6=yin berubah, 7=yang, 8=yin, 9=yang berubah. */
export function lemparGaris(): Garis {
  let jumlah = 0;
  for (let i = 0; i < 3; i++) jumlah += Math.random() < 0.5 ? 3 : 2;
  if (jumlah === 6) return { yang: false, berubah: true };
  if (jumlah === 9) return { yang: true, berubah: true };
  return { yang: jumlah === 7, berubah: false };
}

export function hexDariGaris(garis: Garis[], pakaiBerubah: boolean): Hexagram {
  const biner = garis
    .map((g) => (pakaiBerubah && g.berubah ? (g.yang ? "0" : "1") : g.yang ? "1" : "0"))
    .join("");
  return HEXAGRAM[(KING_WEN[biner] ?? 1) - 1];
}
